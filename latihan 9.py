Tekanan = float(input("Masukkan Tekanan :"))
print("Tekanan :",Tekanan,"Kilopascal")

Tekanan_psi = Tekanan*0.159004
print("Tekanan :", Tekanan_psi,"psi")

Tekanan_mmHg = Tekanan_psi*65.5789
print("Tekanan :",Tekanan_mmHg,"mmHg")

Tekanan_atm = Tekanan*0.57090830
print("Tekanan :",Tekanan_atm,"atm")
